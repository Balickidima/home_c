#include <stdio.h>

//Задача E3 ДЗ
//Считать массив из 10 элементов и найти в нем максимальный и минимальный элементы и их номера.

//Формат входных данных
//10 целых чисел через пробел

int main() {
    int a[10]; //~Создадим массив из 10 элементов
    
    //~Ожидает ввода знаений переменных от пользователя и ложим в масив
    scanf("%d %d %d %d %d %d %d %d %d %d"
    , &a[0], &a[1], &a[2], &a[3], &a[4], &a[5], &a[6], &a[7], &a[8], &a[9]); 
    
    
    // Инициализация первым элементом
    int max_val = a[0], min_val = a[0];
    int max_index = 0, min_index = 0;
    
    // Поиск максимума и минимума
    for(int i = 1; i < 10; i++) {
        if(a[i] > max_val) {
            max_val = a[i];
            max_index = i;
        }
        if(a[i] < min_val) {
            min_val = a[i];
            min_index = i;
        }
    }
    
    // Вывод с номерами, начиная с 1
    printf("%d %d %d %d", 
           max_index + 1, max_val, 
           min_index + 1, min_val);
    
    return 0;
}
