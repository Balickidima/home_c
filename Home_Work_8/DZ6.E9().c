#include <stdio.h>

//Задача E9 ДЗ
//Циклический сдвиг массива вправо
//Считать массив из 10 элементов и выполнить циклический сдвиг ВПРАВО на 1.


int main() {
    int a[10];
    
     //~Ожидает ввода знаений переменных от пользователя и ложим в масив
    scanf("%d %d %d %d %d %d %d %d %d %d"
    , &a[0], &a[1], &a[2], &a[3], &a[4], &a[5], &a[6], &a[7], &a[8], &a[9]); 
    
    // Сохраняем последний элемент
    int last = a[9];
    
    // Сдвигаем элементы вправо на 1 позицию
    for (int i = 8; i >= 0; i--) {
        a[i + 1] = a[i];
    }
    
    // Помещаем сохранённый элемент в начало
    a[0] = last;
    
    // Выводим результат
    for (int i = 0; i < 9; i++) {
        printf("%d ", a[i]);
    }
    printf("%d", a[9]); // Последний элемент без пробела
    
    return 0;
}
