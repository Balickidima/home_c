/*Написать функцию и программу демонстрирующую работу данной функции. Вывести в порядке возрастания цифры, входящие в десятичную запись натурального числа N, не более 1000 цифр. Цифра пробел сколько раз данная цифра встречается в числе.

Формат входных данных
Натуральное число не более 1000 цифр
*/

void Funksion_1(char *number) {
    int counts[10] = {0}; // Массив для подсчета цифр 0-9

    // Проход по каждому символу (цифре) в массиве number
    for (int i = 0; i < strlen(number); i++) {
        if (isdigit(number[i])) {
            int digit = number[i] - '0'; // Преобразование символа в число
            counts[digit]++; // Увеличение счетчика для данной цифры
        }
    }

    // Вывод цифр и их количества в порядке возрастания (0-9)
    for (int i = 0; i < 10; i++) {
        if (counts[i] > 0) {
            printf("%d %d\n", i, counts[i]);
        }
    }
}

int main() {
    char number[1001]; // Массив для хранения числа (до 1000 цифр + '\0')

   //printf("Введите число: "); //для проверки
    if (fgets(number, sizeof(number), stdin) == NULL) {
    //    printf("Ошибка ввода!\n"); //для проверки
        return 1;
    }

    // Удаление символа новой строки
    number[strcspn(number, "\n")] = '\0';


    // Вызов функции для обработки массива символов
    Funksion_1(number);

    return 0;
}



